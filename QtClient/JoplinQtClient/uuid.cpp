#include <stable.h>
#include "uuid.h"

namespace jop {
namespace uuid {

QUuid fromString(const QString& s) {
	// {xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx}
	QString mod = s;
	mod.insert(8, '-');
	mod.insert(13, '-');
	mod.insert(18, '-');
	mod.insert(23, '-');
	mod = "{" + mod + "}";

	qDebug() << mod;

	return QUuid(mod);
}

}
}
